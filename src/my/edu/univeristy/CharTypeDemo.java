package my.edu.univeristy;

public class CharTypeDemo {

    public static void main(String[] args) {
        var myChar = 'a';
        System.out.println("My Char: " + myChar);

        var varChar = '\u0021';
        System.out.println("Var Char: " + varChar);

        var varDecimal = 33;
        System.out.println("Var Decimal: " + varDecimal);

        var varCharSymbol = '!';

        System.out.println("Var Symbol: " + varCharSymbol);

        int varInteger = '!';
        System.out.println("VarInteger " + varInteger);

        int letter = 'A';
        System.out.println("Letter: " + letter);

    }

}
