package my.edu.univeristy;

public class FloatTypeDemo {
    public static void main(String[] args) {

        /*
        Float types
        - Fl[oat
        - Double
         */

        float numberFloat = (float)3.4028235E38D;
        System.out.println("Number Float: " + numberFloat);
        System.out.println("Max Float: " + Float.MAX_VALUE);
        System.out.println("Min Float: " + Float.MIN_VALUE);

        double numberDouble = 1.7976931348623157E308;
        System.out.println("Number Double: " + numberDouble);
        System.out.println("Max Float: " + Double.MAX_VALUE);
        System.out.println("Min Float: " + Double.MIN_VALUE);



    }

}
