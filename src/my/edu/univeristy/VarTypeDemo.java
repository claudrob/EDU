package my.edu.univeristy;

public class VarTypeDemo {

    public static void main(String[] args) {

        var numberInt = 10;
        System.out.println("Var: " + numberInt);

        var numberDouble = 10.0;
        System.out.println("Var: " + numberDouble);

        var numberFloat = 10.0F;

        System.out.println("Var: " + numberFloat);

    }

}
